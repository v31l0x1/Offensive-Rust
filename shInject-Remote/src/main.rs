fn pause() {
    use std::io::{self, Write};
    let mut stdout = io::stdout();
    write!(stdout, "[+] Press Enter to continue...").unwrap();
    stdout.flush().unwrap();
    let _ = io::stdin().read_line(&mut String::new());
}

fn find_pid(process_name: String) -> u32 {
    let s = System::new_all();
    let mut pid = 0;
    for (pids, process) in s.processes() {
        // println!("[{pid}] {:?}", process.name());
        if process.name().to_ascii_lowercase() == process_name.to_ascii_lowercase().as_str() {
            println!("[+] Found process {:?} with PID: {}", process.name(), pids);
            pid = pids.as_u32();
            break;
        }
    }
    return pid;
}