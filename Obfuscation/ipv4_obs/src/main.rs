fn encode(bytes: &[u8]) -> Vec<String> {
    let mut result = Vec::new();
    for c in bytes.chunks(4) {
        let mut arr = [0u8; 4];
        arr[..c.len()].copy_from_slice(c);
        result.push(format!("{}.{}.{}.{}", arr[0], arr[1], arr[2], arr[3]));
    }
    result
}

fn decode(addrs: &[&str]) -> Vec<u8> {
    let mut result = Vec::with_capacity(addrs.len() * 4);

    for addr in addrs {
        let parts: Vec<u8> = addr.split(".").map(|p| p.parse().unwrap()).collect();
        result.extend_from_slice(&parts);
    }
    result
}

