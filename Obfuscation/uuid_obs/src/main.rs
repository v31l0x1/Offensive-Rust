fn encode(bytes: &[u8]) -> Vec<String> {
    let encoded = bytes.chunks(16).map(|c| {
        let mut buf = [0u8; 16];
        buf[..c.len()].copy_from_slice(c);
        format!(
            "{:02x}{:02x}{:02x}{:02x}-{:02x}{:02x}-{:02x}{:02x}-{:02x}{:02x}-{:02x}{:02x}{:02x}{:02x}{:02x}{:02x}",
            buf[0], buf[1], buf[2], buf[3],
            buf[4], buf[5],
            buf[6], buf[7],
            buf[8], buf[9],
            buf[10], buf[11], buf[12], buf[13], buf[14], buf[15]
        )
    }).collect();

    encoded
}

fn decode(encoded: &[&str]) -> Vec<u8> {
    let mut decoded: Vec<_> = Vec::new();
    for line in encoded {
        let bytes: Vec<u8> = line
            .split('-')
            .flat_map(|chunk| {
                (0..chunk.len())
                    .step_by(2)
                    .map(move |i| u8::from_str_radix(&chunk[i..i + 2], 16).unwrap())
            })
            .collect();
        decoded.extend(bytes);
    }
    decoded
}

fn print_ips(encoded: Vec<String>) {
    println!("const ENCODED_MAC: [&str; {}] = [", encoded.len());
    for line in encoded {
        println!("    \"{}\",", line);
    }
    println!("];");
}

fn hex_dump(bytes: &[u8]) {
    for (i, byte) in bytes.iter().enumerate() {
        if i % 16 == 0 {
            print!("\n  {:08x}: ", i);
        }
        print!("{:02x} ", byte);
    }
    println!();
}

