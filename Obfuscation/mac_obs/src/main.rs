fn encode(bytes: &[u8]) -> Vec<String> {
    let mut encoded = bytes
        .chunks(6)
        .map(|c| {
            let mut buf = [0u8; 6];
            buf[..c.len()].copy_from_slice(c);
            (0..6)
                .map(|i| format!("{:02x}", buf[i]))
                .collect::<Vec<_>>()
                .join(":")
        })
        .collect();

    encoded
}

fn decode(encoded: &[&str]) -> Vec<u8> {
    let mut decoded = Vec::new();
    for line in encoded {
        let bytes: Vec<u8> = line
            .split(':')
            .map(|s| u8::from_str_radix(s, 16).unwrap())
            .collect();
        decoded.extend(bytes);
    }
    decoded
}

fn print_ips(encoded: Vec<String>) {
    println!("const ENCODED_MAC: [&str; {}] = [", encoded.len());
    for line in encoded {
        println!("    \"{}\",", line);
    }
    println!("];");
}

fn hex_dump(bytes: &[u8]) {
    for (i, byte) in bytes.iter().enumerate() {
        if i % 16 == 0 {
            print!("\n  {:08x}: ", i);
        }
        print!("{:02x} ", byte);
    }
    println!();
}

