fn print_ips(encoded: Vec<String>) {
    println!("const ENCODED_IPS: [&str; {}] = [", encoded.len());
    for line in encoded {
        println!("    \"{}\",", line);
    }
    println!("];");
}

fn hex_dump(bytes: &[u8]) {
    for (i, byte) in bytes.iter().enumerate() {
        if i % 16 == 0 {
            print!("\n  {:08x}: ", i);
        }
        print!("{:02x} ", byte);
    }
    println!();
}

