fn encode(bytes: &[u8]) -> Vec<String> {
    bytes
        .chunks(16)
        .map(|c| {
            c.chunks(2)
                .map(|pair| {
                    if pair.len() == 2 {
                        format!("{:02x}{:02x}", pair[0], pair[1])
                    } else {
                        format!("{:02x}", pair[0])
                    }
                })
                .collect::<Vec<_>>()
                .join(":")
        })
        .collect()
}

fn decode(strings: &[&str]) -> Vec<u8> {
    let mut result = Vec::new();
    for s in strings {
        for segment in s.split(':') {
            let high = u8::from_str_radix(&segment[0..2], 16).unwrap();
            let low = u8::from_str_radix(&segment[2..4], 16).unwrap();
            result.push(high);
            result.push(low);
        }
    }
    result
}

